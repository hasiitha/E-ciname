<p>
Crawl is a 2019 American disaster horror film directed by Alexandre Aja and written by Michael and Shawn Rasmussen.
It stars Kaya Scodelario and Barry Pepper as a daughter and father, who along with their dog are hunted by alligators after 
becoming trapped in their home during a Category 5 hurricane.

The film was announced in May 2018, with Sam Raimi producing through his Ghost House Pictures banner, and Aja attached as director.
Scodelario and Pepper joined the cast in July 2018, with other actors being added later that summer. Principal photography began in August 2018 and wrapped in September 2018.

The film was theatrically released in the United States on July 12, 2019, by Paramount Pictures. 
It has grossed $70.2 million worldwide and received generally positive reviews, with critics praising the performances (particularly Scodelario) and 
called it "satisfying and a lot of fun."

