<p>

Aladdin is a 2019 American musical fantasy film produced by Walt Disney Pictures. Directed by Guy Ritchie, who co-wrote the screenplay with John August, 
it is a live action adaptation of Disney's 1992 animated film of the same name, which is based on the eponymous tale from One Thousand and One Nights. 
The film stars Will Smith,Mena Massoud,Naomi Scott, ]Marwan Kenzari,Navid Negahban,Nasim Pedrad,Billy Magnussen and Numan Acar, as well as the voices of Alan Tudyk and Frank Welker.
The plot follows Aladdin, a street urchin, as he falls in love with Princess Jasmine, befriends a wish-granting Genie, and battles the wicked Jafar.

In October 2016, Disney announced Ritchie would direct a live-action Aladdin remake. Smith was the first member of the cast to join, signing on to portray Genie in July 2017, 
and Massoud and Scott were confirmed for the two lead roles later that month. Principal photography began that September at Longcross Studios in Surrey, England, 
also filming in the Wadi Rum Desert in Jordan, and lasted until January 2018. Additional filming and pick-ups took place in August 2018.

Aladdin was theatrically released in the United States on May 24, 2019. It has grossed $1 billion worldwide, becoming the fifth highest-grossing film of 2019, 
and the 34th highest-grossing film of all-time. The film received mixed reviews from critics, 
who praised the performances of Smith, Massoud and Scott, the costumes and the musical score, but criticized Ritchie's direction and the CGI effects. 
Critics were also divided on the deviations from the original animated film, such as the characterization of Jafar and Kenzari's casting.

